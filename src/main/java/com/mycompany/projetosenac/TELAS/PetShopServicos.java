    private DefaultTableModel montarTabela(List<TiposServicos> listar) {
        DefaultTableModel tabela = new DefaultTableModel();
        tabela.setColumnIdentifiers(new String[]{"tipo", "tutorRes", "horario"});

        if (listar != null) {
            for (TiposServicos l : listar) {
                tabela.addRow(new Object[]{
                    l.gettipo(),
                    l.gettutorRes(),
                    l.gethorario()
                });

            }
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum serviço encontrado!!");
        }
        return tabela;
    }

