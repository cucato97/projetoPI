    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed

        try {
            int linhaSelecionada = jTableServicos.getSelectedRow();

            if (linhaSelecionada >= 0) {
                // Suponha que a coluna 1 seja o campo "tipo"
                String tipo = jTableServicos.getValueAt(linhaSelecionada, 1).toString();

                int resposta = JOptionPane.showConfirmDialog(this, "Deseja mesmo excluir o serviço com tipo: " + tipo + "?");

                if (resposta == 0) {
                    ServicosJPA servicosJPA = new ServicosJPA();

                    // Excluir pelo valor da coluna "tipo"
                    ServicosJPA.excluirPorTipo(tipo);

                    JOptionPane.showMessageDialog(this, "Registro excluído com sucesso");

                    jTextFielPesquisar2ActionPerformed(evt); 
                }
            }

            jTableServicos.setModel(montarTabela(ServicosJPA.listar()));
            jScrollPane2.setViewportView(jTableServicos);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
        }
    


    }//GEN-LAST:event_jButtonExcluirActionPerformed

